int main(void)
{
	uint8_t payload[150] = {0};
	uint8_t bstr_ex[] = {0x01, 0x23, 0x45, 0x67, 0x89, 0xab, 0xcd, 0xef,
		 0xfe, 0xdc, 0xba, 0x98, 0x76, 0x54, 0x32, 0x10,
		 0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77,
		 0x88, 0x99, 0xaa, 0xbb, 0xcc, 0xdd, 0xee, 0xff};

	ZCBOR_STATE_E(state_e, 3, payload, sizeof(payload), 0);
	ZCBOR_STATE_D(print_state1, 3, payload, sizeof(payload), 30, 0);
	ZCBOR_STATE_D(print_state2, 3, suit_example, sizeof(suit_example), 30, 0);

	state_e->constant_state->stop_on_error = true;

	zcbor_list_start_encode(state_e, 0);
	zcbor_int32_put(state_e, 0);
	zcbor_int32_put(state_e, 0x1234);
	zcbor_int64_put(state_e, 9876543210);
	zcbor_bstr_put_arr(state_e, bstr_ex);
	zcbor_tstr_put_lit(state_e, "Hello World");
	zcbor_tag_encode(state_e, &(uint32_t){9});
	zcbor_tag_put(state_e, 0x1234);
	zcbor_bool_put(state_e, true);
	zcbor_bool_put(state_e, false);
	zcbor_float16_put(state_e, 3.14);
	zcbor_float32_put(state_e, 10.5);
	zcbor_float64_put(state_e, 12.7);
	zcbor_nil_put(state_e, NULL);
	zcbor_undefined_put(state_e, NULL);
	zcbor_bstr_start_encode(state_e);
	zcbor_bstr_end_encode(state_e, NULL);
	zcbor_list_start_encode(state_e, 1);
	zcbor_map_start_encode(state_e, 2);
	zcbor_tstr_put_lit(state_e, "Key0");
	zcbor_float32_put(state_e, 0.0);
	zcbor_int32_put(state_e, 1);
	zcbor_tstr_put_lit(state_e, "Value1");
	zcbor_map_end_encode(state_e, 2);
	zcbor_list_end_encode(state_e, 1);
	bool ret = zcbor_list_end_encode(state_e, 0);

	if (!ret) {
		printk("Encode error: %d\r\n", zcbor_peek_error(state_e));
		return 1;
	}

	ret = zcbor_diag_print(print_state1);

	if (!ret) {
		printk("Print error 1: %d\r\n", zcbor_peek_error(print_state1));
		return 1;
	}

	ret = zcbor_diag_print(print_state2);

	if (!ret) {
		printk("Print error 2: %d\r\n", zcbor_peek_error(print_state2));
		return 1;
	}

	return 0;
}
